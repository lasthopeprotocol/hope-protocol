    log.ok(`ğŸ’µ SOL used:       ${solAvailable.toFixed(6)} SOL`);
    log.ok(`ğŸ“¤ Sent to loser:  ${halfAmount.toFixed(2)} $HOPE`);
    log.ok(`ğŸ”¥ Burned:         ${halfAmount.toFixed(2)} $HOPE`);
    log.ok(`ğŸ“œ Send TX:        ${txSend}`);
